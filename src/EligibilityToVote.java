public class EligibilityToVote
//This program is to check eligibility to vote of a person
{
    public static void main(String[] args)
    {
        int age = 20;//declaring variable
//if else method
        System.out.println("Age of a person: " + age);//applying condition using if else method

        if (age >= 18)
        {
         System.out.println("You are eligible to vote");//condition1
        }
        else
        {
          System.out.println("You are not eligible to vote");//condition2

        }

    }
}